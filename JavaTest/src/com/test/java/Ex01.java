
//패키지 선언 > 현재 자바 파일이 어떤 패키지 내에 위치하는지 표현
package com.test.java;

//클래스 > 코드의 집합(내용물)
//public class Ex01 : 클래스 선언부(header)
//{} : 클래스 몸통(body), 구현부
public class Ex01 {
	//메소드(Method) > 코드의 집합
	public static void main(String[] args) {
		
//		명령어 > 문장 단위 구성
//		System : 클래스
//		. : 멤버 접근 연산자(Member Acces Operator) 부모.자식
//		out : 필드(filed)
//		println() : 메소드 호출
//		"안녕하세요" : 문자열 리터럴
		System.out.println("안녕하세요");
		/*
		 * 특수문자(영문 표현 알아두기)
		 * ~(틸드),!,@(at),#,$,%,^(xor),&,*(애스터리스크),-,_(언더라인),+,=
		 * ;,:,',",?,/(slash),\(back slash), `(역따옴표, back tick, back quote), |(pipe, vertical bar, or)
		 * (소괄호),{중괄호},[대괄호],<화살표괄호>
		 
		 * 폰트 > 굴림체는 쓰지말 것(가독성이 매우 안좋음)
		 * 
		 * 이클립스 > 코드 자동 완성(Ctrl + Space)
		 * 현재 상태에서 사용 가능한 문법만 보여준다.
		 * 
		 * 단축키
		 * Ctrl+Alt+방향키(위/아래) > 방향키대로 복사
		 * Alt+방향키(위/아래) > 방향키대로 이동
		 * Ctrl + D > 라인 삭제
		 * Ctrl + Shift + L > 단축키 목록
		 * Ctrl + F6 > 탭 이동
		 */
		System.out.println();
	}

}
